export function getSampleBlogsResponse(skip: number = 0, limit: number = 10) {
  const startIndex = skip;
  const endIndex = skip + limit;
  const paginatedBlogs = sampleBlogs.slice(startIndex, endIndex);
  
  return {
    success: true,
    data: {
      list: paginatedBlogs,
      total: sampleBlogs.length,
      skip: skip,
      limit: limit,
      hasMore: endIndex < sampleBlogs.length
    }
  };
}

